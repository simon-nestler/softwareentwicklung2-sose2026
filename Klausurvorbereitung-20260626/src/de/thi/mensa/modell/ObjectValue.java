package de.thi.mensa.modell;

public class ObjectValue {
    private String value;

    public ObjectValue(String value) {
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return value;
    }
}
